    @mcp.tool()
    async def search_products_in_inventory(
        id: Optional[int] = None,
        name: Optional[str] = None,
        sku: Optional[str] = None,
        supplier_id: Optional[int] = None,
        low_stock: Optional[bool] = None,
        price: Optional[float] = None,
        final_price: Optional[float] = None,
        tax_rate: Optional[float] = None,
        tax_rate_display: Optional[str] = None,
        updated_after: Optional[str] = None # Format: ISO
    ) -> List[Dict[str, Any]]:
        """
        Search products using any specific parameter from the database.
        Returns the full product object for the matches found.
        """
        data = await products_api.get_all_products()
        results = data

        if id: results = [p for p in results if p["id"] == id]
        if name: results = [p for p in results if name.lower() in p["name"].lower()]
        if sku: results = [p for p in results if sku.lower() in p["sku"].lower()]
        if supplier_id: results = [p for p in results if p["supplier"] == supplier_id]
        if low_stock is not None: results = [p for p in results if p["low_stock"] == low_stock]
        if price: results = [p for p in results if float(p["price"]) == price]
        if final_price: results = [p for p in results if float(p["final_price"]) == final_price]
        if tax_rate: results = [p for p in results if float(p["tax_rate"]) == tax_rate]
        if tax_rate_display: results = [p for p in results if tax_rate_display.lower() in p["tax_rate_display"].lower()]
        if updated_after: results = [p for p in results if p["updated_at"] >= updated_after]

        return results[:20]

