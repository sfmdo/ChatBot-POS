    # !  CUSTOMERS MODULE

    def clean_money(value: str) -> float:
        if not value: return 0.0
        return float(re.sub(r'[^\d.]', '', str(value)))

    @mcp.tool()
    async def get_total_customer_count() -> Dict[str, int]:
        """Returns the total number of registered customers."""
        data = await customers_api.get_all_customers()
        return {"total_customers": len(data)}

    @mcp.tool()
    async def search_customers(
        id: Optional[int] = None,
        name: Optional[str] = None,
        is_frequent: Optional[bool] = None,
        has_debt: Optional[bool] = None,
        min_points: Optional[int] = None,
        birth_month: Optional[int] = None
    ) -> List[Dict[str, Any]]:
        """
        Advanced customer search with unified name lookup and financial filters.
        """
        data = await customers_api.get_all_customers()
        results = data

        if id: 
            results = [c for c in results if c["id"] == id]
    
        if name:
            query = name.lower()
        else:
            query=""

        results = [
            c for c in results 
            if query in f"{c['first_name']} {c['last_name']}".lower()
            ]

        if is_frequent is not None: 
            results = [c for c in results if (c["frequent_customer"] == "Yes") == is_frequent]
    
        if has_debt is not None:
            results = [c for c in results if (clean_money(c["credit_used"]) > 0) == has_debt]
    
        if min_points:
            results = [c for c in results if int(c["current_points"]) >= min_points]
        
        if birth_month:
            results = [c for c in results if int(c["birth_date"].split("-")[1]) == birth_month]

        return results[:20]

